

class Calculador
{

    double sumar(double a, double b)
    {
        return a + b;
    }
    double resta(double a, double b)
    {
        return a - b;
    }
    double Multiplicar(double a, double b)
    {
        return a * b;
    }
    double Dividir(double a, double b)
    {
        return a / b;
    }
}